#include <iostream>
using namespace std;

#include "solution.h"

int main()
{
	int t;
	cin >> t;
	
	while (t--)
	{
		int size;
		cin >> size;
		int *input = new int[size];

		for (int i = 0; i < size; i++)
		{
			cin >> input[i];
		}

		cout << findDuplicate(input, size) << endl;
	}

	return 0;
}


int findDuplicate(int *arr, int n)
{
    int sum=0,res=0;
    
     res=(n-2)*(n-1)/2;
    
    for(int i=0;i<n;i++)
    {
        sum+=arr[i];
    }
    return sum-res;
}
